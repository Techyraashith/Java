public class Relational {
    public static void main(String[] args) {
      /* Relational Operator  >, <, <=, >=, ==, !=*/

      System.out.println("This Condition 8 < 5 is : "+(8 < 5));
      System.out.println("This Condition 8 > 5 is : "+(8 > 5));
      System.out.println("This Condition 8 == 8 is : "+(8 == 8));
      System.out.println("This Condition 8 != 8 is : "+(8 != 8));
      
    }
}
